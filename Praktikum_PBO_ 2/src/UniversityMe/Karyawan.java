package UniversityMe;

public class Karyawan {

    String nama;
    String alamat;
    String jabatan;
    Double gaji;

    void tampilkanNama(String namaKaryawan){
        nama = namaKaryawan;
    }
    void tampilkanJabatan(String alamatKaryawan){
        alamat = alamatKaryawan;
    }
    void tampilkanAlamat(String jabatanKaryawan){
        jabatan = jabatanKaryawan;
    }
    void tampilkanGaji(Double gajiKaryawan){
        gaji = gajiKaryawan;
    }   

}